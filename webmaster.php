                        if (entry.isIntersecting) {

                            let lazyloadBackground = entry.target;

                            const lazyloadSelector = lazyloadBackground.getAttribute(dataAttribute);

                            if (lazyloadSelector) {

                                lazyloadBackground = entry.target.querySelector(lazyloadSelector);

