                        }

                        lazyloadBackgroundObserver.unobserve(entry.target);

                    }

                });

            }, {

                rootMargin: '100px 0px 100px 0px'

            });

            lazyloadBackgrounds.forEach((lazyloadBackground) => {

                lazyloadBackgroundObserver.observe(lazyloadBackground);

            });

        };

