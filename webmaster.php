        <script>
            let currentIndex = 0; // Track the active tab index
            const tabs = document.querySelectorAll('.tab');
            const activeImage = document.getElementById('active-image');
            const autoplayInterval = 3000; // Set autoplay interval (in milliseconds)

            // Function to activate a tab and update the image
            function activateTab(index) {
                // Remove active class from all tabs
                tabs.forEach(tab => tab.classList.remove('active'));

                // Add active class to the clicked tab
                tabs[index].classList.add('active');

                // Update the image source based on the active tab's data-image attribute
                const newImage = tabs[index].getAttribute('data-image');
                activeImage.style.opacity = 0; // Smooth fade-out
                setTimeout(() => {
                    activeImage.src = newImage; // Change the image source
                    activeImage.style.opacity = 1; // Smooth fade-in
                }, 300); // Sync with CSS transition
            }

            // Autoplay function to cycle through tabs
            function autoplayTabs() {
                currentIndex = (currentIndex + 1) % tabs.length; // Loop back to the first tab after the last one
                activateTab(currentIndex);
            }

            // Start autoplay
            let autoplay = setInterval(autoplayTabs, autoplayInterval);

            // Add click event listeners to tabs (pauses autoplay when clicked)
            tabs.forEach((tab, index) => {
                tab.addEventListener('click', () => {
                    clearInterval(autoplay); // Pause autoplay
                    activateTab(index); // Activate clicked tab
                    currentIndex = index; // Update current index
                    autoplay = setInterval(autoplayTabs, autoplayInterval); // Restart autoplay
                });
            });

            // Initial activation
            activateTab(currentIndex);
        </script>
